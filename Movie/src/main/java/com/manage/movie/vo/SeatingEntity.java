package com.manage.movie.vo;

public class SeatingEntity {
	int se_id;
	String se_name;
	String se_column;
	String se_row;
	String se_type;
	public int getSe_id() {
		return se_id;
	}
	public void setSe_id(int se_id) {
		this.se_id = se_id;
	}
	public String getSe_name() {
		return se_name;
	}
	public void setSe_name(String se_name) {
		this.se_name = se_name;
	}
	public String getSe_column() {
		return se_column;
	}
	public void setSe_column(String se_column) {
		this.se_column = se_column;
	}
	public String getSe_row() {
		return se_row;
	}
	public void setSe_row(String se_row) {
		this.se_row = se_row;
	}
	public String getSe_type() {
		return se_type;
	}
	public void setSe_type(String se_type) {
		this.se_type = se_type;
	}
}
