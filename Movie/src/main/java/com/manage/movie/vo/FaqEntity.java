package com.manage.movie.vo;

public class FaqEntity {
	int fq_id;
	String fq_group;
	String fq_question;
	String fq_answer;
	public int getFq_id() {
		return fq_id;
	}
	public void setFq_id(int fq_id) {
		this.fq_id = fq_id;
	}
	public String getFq_group() {
		return fq_group;
	}
	public void setFq_group(String fq_group) {
		this.fq_group = fq_group;
	}
	public String getFq_question() {
		return fq_question;
	}
	public void setFq_question(String fq_question) {
		this.fq_question = fq_question;
	}
	public String getFq_answer() {
		return fq_answer;
	}
	public void setFq_answer(String fq_answer) {
		this.fq_answer = fq_answer;
	}
	
}
